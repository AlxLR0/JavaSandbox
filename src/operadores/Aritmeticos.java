package operadores;

public class Aritmeticos {
    static void main() {
        //en este caso se tiene que poner el tipo de dato (int en este caso) ya que con var no funciona
        int a = 5, b= 3, resultado;

        // + suma, - resta, *multiplicacion, / division, % modulo(residuo de la division)
        resultado = a+b;
        System.out.println("resultado = " + resultado);
    }
}
